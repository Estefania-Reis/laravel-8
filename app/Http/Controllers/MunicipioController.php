            $data = Municipio::where('naran','LIKE','%' .$request->search.'%')->paginate(5);
            Session::put('halaman_url', request()->fullUrl());
        }else{
            $data = Municipio::paginate(5);
           
            Session::put('halaman_url', request()->fullUrl());
        }
        return view('data_extra\municipio\index', compact('data'));
